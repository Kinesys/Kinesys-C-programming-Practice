#include <stdio.h>

int main(void) {
	printf("001 :　printf 명령을 연습합니다.\n", 123);
	printf("002 :　값 %d 는 16진수로 <%x>, 8진수로 <%o> 입니다.\n", 19, 19, 19);
	printf("003 :　식 %d * %d 의 결과는 %d 입니다.\n", 7, 9, 7 * 9);
	printf("004 :　숫자 %d의 ASCII 문자는 '%c' 입니다.\n", 78, 78);
	printf("005 :　값 %d 은 +%d, %+d \n", -19, -19, -19);

	printf("006 :　C언어에서 사용하는 \"특수 문자\" 에는 여러가지가 있습니다.\n");
	printf("007 :　\t특수 문자 바로 앞에는 언제나 \\ 가 와야 합니다.");

	printf("008 :　%%c를 사용한 결과 : %c\n", 'a');
	printf("009 :　%%s를 사용한 결과 : %s\n", "즐거운 C언어");

	printf("010 :　%%f를 사용한 결과 : %f\n", 0.123456);
	printf("011 :　%%f를 사용한 결과 : %f\n", 0.123456789);

	printf("012 :　%%o를 사용한 결과 : %o\n", 123);
	printf("013 :　%%x를 사용한 결과 : %x\n", 123);

	printf("014 :　%%g를 사용한 결과 : %g\n", 0.001234);
	printf("015 :　%%g를 사용한 결과 : %g\n", 0.00001234);
	printf("016 :　%%G를 사용한 결과 : %G\n", 0.000001234);

	printf("017 :　%%d를 사용한 결과 : |%d|\n", 123);
	printf("018 :　%%7d를 사용한 결과 : |%7d|\n", 123);
	printf("019 :　%%+7d를 사용한 결과 : |%+7d|\n", 123);
	printf("020 :　%%-7d를 사용한 결과 : |%-7d|\n\n", 123);

	printf("021 :　%%f를 사용한 결과 : |%f|\n", 1.23);
	printf("022 :　%%.1f를 사용한 결과 : |%.1f|\n", 1.23);
	printf("023 :　%%7.2f를 사용한 결과 : |%7.2f|\n", 1.23);
	printf("024 :　%%+7.2f를 사용한 결과 : |%+7.2f|\n", 1.23);
	printf("025 :　%%-7.2f를 사용한 결과 : |%-7.2f|\n\n", 1.23);

	return 0;

}

