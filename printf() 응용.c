/*


%c
하나의 문자, 홑따옴표로 묶어준다
%s
문자열, 쌍 따옴표로 묶어준다.
%d
부호 있는 10진 정수
%i
부호 있는 10진 정수 (%d와 동일)
%f
고정 소수점으로 표현한 실수 (소수점 이하 6자리까지 표현)
%o
부호 없는 8진 정수
%u
부호 없는 10진 정수
%x
부호 없는 16진 정수 (소문자 사용)
%X
부호 없는 16진 정수 (대문자 사용)
%e
부동 소수점으로 표현한 실수 (e-표기법)
%E
부동 소수점으로 표현한 실수 (E-표기법)
%g
값에 따라 %f나 %e를 사용함.
%G
값에 따라 %f나 %E를 사용함.
%%
퍼센트(%) 기호 출력
▷ 출력 필드의 폭 설정
서식 지정자의 '%' 기호와 타입을 나타내는 영문자 사이에 숫자를 추가하여 출력되는 필드의 폭을 직접 설정할 수 있다. (예, %3d, %5.2f) 내부의 숫자는 오른쪽 정렬이 기본이며, 숫자 앞에 '-' 기호를 붙이면 왼쪽 정렬로 변경된다. 또한, 숫자 앞에 '+' 기호를 붙이면 숫자를 오른쪽 정렬한 상태에서 양수에는 '+' 기호를, 음수에는 '-' 기호를 붙여서 출력한다. 이때 소수 부분의 숫자는 출력되는 소수의 자릿수를 명시한다.

*/


